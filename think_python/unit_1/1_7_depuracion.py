""" 1.7. Depuración 

Los programadores cometen errores. Por razones curiosas, los errores de programación se 
llaman bugs y el proceso para detectarlos se llama depuración .

La programación, y en especial la depuración, a veces genera emociones fuertes. Si te 
encuentras con un error complejo, podrías sentirte enojado, triste o avergonzado.

Prepararse para estas reacciones podría ayudarle a afrontarlas. Un enfoque es pensar en la 
computadora como un empleado con ciertas fortalezas, como la velocidad y la precisión, y 
debilidades particulares, como la falta de empatía y la incapacidad de comprender el 
panorama general.

Tu trabajo es ser un buen gerente: encuentra maneras de aprovechar las fortalezas y 
mitigar las debilidades. Y encuentra maneras de usar tus emociones para abordar el 
problema, sin dejar que tus reacciones interfieran con tu capacidad para trabajar 
eficazmente.

Aprender a depurar puede ser frustrante, pero es una habilidad valiosa que resulta útil 
para muchas actividades más allá de la programación. Al final de cada capítulo hay una 
sección, como esta, con mis sugerencias para la depuración. ¡Espero que te sean útiles! """
