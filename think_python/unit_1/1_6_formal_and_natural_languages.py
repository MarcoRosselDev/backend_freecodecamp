""" 1.6. Lenguajes formales y naturales 

Los idiomas naturales son los que se hablan, como el inglés, el español y el francés. No fueron diseñados por personas, 
sino que evolucionaron de forma natural.

Los lenguajes formales son lenguajes diseñados por personas para aplicaciones específicas. Por ejemplo, la notación que 
usan los matemáticos es un lenguaje formal especialmente eficaz para denotar relaciones entre números y símbolos. De igual 
manera, los lenguajes de programación son lenguajes formales diseñados para expresar cálculos.

Aunque los lenguajes formales y naturales tienen algunas características en común, existen diferencias importantes:

Ambigüedad: Los lenguajes naturales están llenos de ambigüedad, que se aborda mediante pistas contextuales y otra 
información. Los lenguajes formales están diseñados para ser casi o completamente inequívocos, lo que significa que 
cualquier programa tiene exactamente un significado, independientemente del contexto.

Redundancia: Para compensar la ambigüedad y reducir los malentendidos, los lenguajes naturales utilizan la redundancia. 
Como resultado, suelen ser verbosos. Los lenguajes formales son menos redundantes y más concisos.

Literalidad: Los lenguajes naturales están llenos de modismos y metáforas. Los lenguajes formales significan exactamente lo 
que dicen.

Dado que todos crecemos hablando lenguas naturales, a veces es difícil adaptarse a las lenguas formales. Las lenguas 
formales son más densas que las naturales, por lo que su lectura lleva más tiempo. Además, la estructura es importante, por 
lo que no siempre es mejor leer de arriba a abajo y de izquierda a derecha. Finalmente, los detalles importan. Pequeños 
errores de ortografía y puntuación, que se pueden evitar en las lenguas naturales, pueden marcar una gran diferencia en una 
lengua formal. """
